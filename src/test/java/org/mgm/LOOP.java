public class LOOP {
	


		
	public static void main(String[] args) {
		
		int i=5 ;

		switch (i) 
	
		{
		case 1:
			System.out.println("My number is " + i);
			break;

			case 2:
				System.out.println("My number is " + i);
				break;
			case 3:
				System.out.println("My number is " + i);
				break;
			case 4:
				System.out.println("My number is " + i);
				break;
			case 5:
				System.out.println("My number is " + i);
				break;
			
			default:
		System.out.println("No case");
		}
		
		
	}
	


}
